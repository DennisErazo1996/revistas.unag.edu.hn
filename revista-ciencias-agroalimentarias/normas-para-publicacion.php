        <p>Los trabajos publicados en la <strong>Revista Ciencias Agroambientales</strong> son el resultado de una
            selección rigurosa,
            por parte del Consejo Editorial en primera instancia y el arbitraje realizado por especialistas nacionales e
            internacionales, mediante el sistema de revisión por pares doble ciego.</p>
        <p>
            Todos los documentos para publicación deberán presentarse electrónicamente al correo
            <strong>rcagroambientales@unag.edu.hn</strong> Además, los autores deberán asegurar mediante carta de
            compromiso que el
            trabajo sometido no ha sido publicado previamente (excepto en forma de resumen, conferencia publicada o
            tesis académica), que no está bajo consideración para publicación en otra revista, que su publicación sea
            aprobada por todos los autores y tácita o explícitamente por las autoridades responsables donde se realizó
            el trabajo, y que, si se acepta, no se publicará en otro lugar en la misma forma, en inglés o en cualquier
            otro idioma.
        </p>
        <p>
            Esta revista opera un único proceso de revisión anónimo. Todas las contribuciones serán inicialmente
            evaluadas por el editor para determinar su idoneidad para la revista. Los artículos que se consideran
            adecuados generalmente se envían a un mínimo de dos revisores expertos independientes para evaluar la
            calidad científica del artículo. El comité editorial es responsable de la decisión final sobre la aceptación
            o rechazo de los artículos. La decisión del comité es definitiva. Los editores no participan en las
            decisiones sobre artículos que han escrito ellos mismos o que han sido escritos por familiares o colegas o
            que se relacionan con productos o servicios en los que el editor tiene interés. Cualquier envío de este tipo
            está sujeto a todos los procedimientos habituales de la revista, y la revisión por pares se realiza
            independientemente del autor correspondiente y sus grupos de investigación.</p>
        <p>Una vez que el Consejo Editorial recibe un manuscrito notificará el acuse de recibido al autor de
            correspondencia en un plazo máximo de 1 semana. Posteriormente dicho manuscrito será sometido al comité
            científico quienes informarán al Comité Editorial la pertinencia de la investigación según los ejes
            temáticos de la revista y de la actualidad científica en su campo, el cual, en un término máximo de 4
            semanas notificará al autor de correspondencia la decisión al respecto. Seguidamente, el manuscrito será
            sometido al respectivo proceso de revisión por los pares evaluadores, quienes remitirán al comité editorial
            sus recomendaciones y observaciones en un plazo máximo de 2 meses.</p>
        <p>
            Una vez evaluado el mérito científico o técnico del manuscrito, el Comité Editor comunicará el dictamen al
            autor de correspondencia y se pueden emitir los siguientes criterios:
